#include "mini_shell.h"

int	exp_check(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == '=')
		{
			if (i == 0)
				return (-1);
			return (1);
		}
		i++;
	}
	return (0);
}
